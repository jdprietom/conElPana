public class Covid19{
    
    // day = d
        int d1,d2,d3,d4,d5,d6;
          
        // percentage = perc, average = avg
        int perc1,perc2,perc3,perc4,perc5,avg;

        void algoritmo(){
            
            perc1 = (d2-d1)*100/d2;
            perc2 = (d3-d2)*100/d3;
            perc3 = (d4-d3)*100/d4;
            perc4 = (d5-d4)*100/d5;
            perc5 = (d6-d5)*100/d6;
            avg = (perc1+perc2+perc3+perc4+perc5)/5;
           
        
    } 
}//fin class Covid19

